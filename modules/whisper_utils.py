def fix_traditional_chinese(text):
    """将繁体中文转换为简体中文"""
    if not text:
        return text
    
    if OPENCC_AVAILABLE:
        return _cc.convert(text)
    else:
        return text.translate(_TRAD_TO_SIMP_FALLBACK)


def transcribe_with_fix(model, audio_path, language='zh', **kwargs):
    """
    使用强化的 initial_prompt 进行转写，并对结果做繁简后处理

    参数:
        model: Whisper 模型实例
        audio_path: 音频文件路径
        language: 语言代码
        **kwargs: 其他传给 model.transcribe 的参数

    返回:
        转写结果字典（text 和 segments 中的 text 已被转换为简体中文）
    """
    transcribe_kwargs = {
        'language': language,
        'initial_prompt': ZH_INITIAL_PROMPT,
        'condition_on_previous_text': False,
    }
    transcribe_kwargs.update(kwargs)

    result = model.transcribe(audio_path, **transcribe_kwargs)

    if 'text' in result:
        result['text'] = fix_traditional_chinese(result['text'])

    if 'segments' in result:
        for seg in result['segments']:
            if 'text' in seg:
                seg['text'] = fix_traditional_chinese(seg['text'])

    return result
